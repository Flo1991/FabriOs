/// register structure for Gpio A module registers; need C representation for correct memory layout
#[repr(C)]
pub struct GpioAB {
    pub moder: u32,
    pub otyper: u32,
    pub ospeedr: u32,
    pub pupdr: u32,
    pub idr: u32,
    pub odr: u32,
    pub bsrr: u32,
    pub lckr: u32,
    pub afrl: u32,
    pub afrh: u32,
    pub brr: u32,
}

impl GpioAB {
    /// method to the instance of Gpio A
    pub fn inst_a() -> &'static mut GpioAB {
        unsafe { &mut *(0x4800_0000 as *mut GpioAB) }
    }
}

